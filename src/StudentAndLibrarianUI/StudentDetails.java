    public void initializeConnector() throws SQLException{
        myConnector=new Connector("jdbc:mysql://localhost:3306/lib_db","root","");
    }
    
    public void getStudentDetails() throws SQLException{
        myConnector.prepStatement("SELECT StudID, AdmissionNo, FirstName, LastName,Email,PhoneNo,Course FROM students");
        rs=myConnector.execQuery();
    }
    
    public void displayAuthorDetails(){
        studentsTable.setModel(DbUtils.resultSetToTableModel(rs));
    }
    
    public void updateStudentDetails() throws SQLException{
        int selectedRow=studentsTable.getSelectedRow();
        int studentID=(int) studentsTable.getValueAt(selectedRow, 0);
        myConnector.prepStatement("UPDATE students SET AdmissionNo=?,FirstName=?,LastName=?,Email=?,PhoneNo=?,Course=? WHERE StudID=?");
        myConnector.pst.setString(1, studentsTable.getValueAt(selectedRow, 1).toString());
        myConnector.pst.setString(2, studentsTable.getValueAt(selectedRow, 2).toString());
        myConnector.pst.setString(3, studentsTable.getValueAt(selectedRow, 3).toString());
        myConnector.pst.setString(4, studentsTable.getValueAt(selectedRow, 4).toString());
        myConnector.pst.setString(5, studentsTable.getValueAt(selectedRow, 5).toString());
        myConnector.pst.setString(6, studentsTable.getValueAt(selectedRow, 6).toString());
        myConnector.pst.setInt(7, studentID);
        
        myConnector.execUpdate();
        JOptionPane.showMessageDialog(this,"Student details updated successfully !");
    }

